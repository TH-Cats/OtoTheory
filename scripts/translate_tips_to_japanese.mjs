const translatedLines = dataLines.map(line => {
  // CSVの最後の列（Tips列）を正しく抽出
  const lastQuoteIndex = line.lastIndexOf('"');
  const firstQuoteIndex = line.indexOf('"', line.lastIndexOf(','));
  
  if (firstQuoteIndex !== -1 && lastQuoteIndex !== -1 && firstQuoteIndex < lastQuoteIndex) {
    const beforeTips = line.substring(0, firstQuoteIndex);
    const tipsContent = line.substring(firstQuoteIndex + 1, lastQuoteIndex);
    const afterTips = line.substring(lastQuoteIndex + 1);
    
    const translatedTips = translations[tipsContent] || tipsContent;
    return beforeTips + '"' + translatedTips + '"' + afterTips;
  }
  
  return line;
});

